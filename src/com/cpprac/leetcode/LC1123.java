    public TreeNode lcaDeepestLeaves(TreeNode root) {
        height(root);
        return findlca(root);
    }

    private TreeNode findlca(TreeNode root) {

        if(root == null)
            return null;

        if(root.left!=null && root.right !=null){
            if(root.left.height==root.right.height)
                return root;
            else if(root.left.height>root.right.height)
                return findlca(root.left);
            else
                return findlca(root.right);
        }
        else if(root.left==null && root.right!=null){
            return findlca(root.right);
        }
        else if(root.left!=null){
            return findlca(root.left);
        }
        return root;
    }

    private void height(TreeNode root) {
        if ( root == null )
            return;
        height(root.left);
        height(root.right);
        int leftHeight = root.left == null ? 0 : root.left.height;
        int rightHeight = root.right == null ? 0 : root.right.height;
        root.height = Math.max(leftHeight,rightHeight )+1;
    }

